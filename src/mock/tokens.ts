function s(seed: number, length: number, vol: number): number[] {
  const out: number[] = [];
  let v = 0.5;
  let r = seed;
  for (let i = 0; i < length; i++) {
    r = (r * 9301 + 49297) % 233280;
    const noise = (r / 233280 - 0.5) * vol;
    v = Math.max(0.05, Math.min(0.95, v + noise));
    out.push(v);
  }
  return out;
}

